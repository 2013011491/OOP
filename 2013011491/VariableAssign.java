
public class VariableAssign {

	public static void main(String[] args) {
		int counter;
		int totalDistance;
		int quotient;
		int remainder;
		counter =3;
		totalDistance=15;
		quotient=totalDistance/counter;
		remainder=totalDistance%counter;
		System.out.println(quotient);
		System.out.println(remainder);

	}

}
